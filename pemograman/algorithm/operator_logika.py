#operator_logika
angka = 5 #logika
print ("hasil logika and pada angka adalah",angka > 3 and angka < 9) #True di karenakan kedua statement sama 
print ("hasil logika or pada angka adalah",angka < 3 or angka > 9) #False dikarenakan tidak ada dari salah satu statement yang benar 
print ("hasil logika not pada angka adalah",not(angka < 5 and angka > 10)) #True dikarenakan logika Not berfungsi membalikan hasil yang diberikan. dari false menjadi true




